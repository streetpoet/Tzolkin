package spstudio.com.tzolkin.core;

public class TzolkinDay {
	
	private int seal;
	private int tone;
	private int kin;
	private String sealName;
	private String toneName;
	private String[] codeArrays;
	public int getSeal() {
		return seal;
	}
	public void setSeal(int seal) {
		this.seal = seal;
	}
	public int getTone() {
		return tone;
	}
	public void setTone(int tone) {
		this.tone = tone;
	}
	public int getKin() {
		return kin;
	}
	public void setKin(int kin) {
		this.kin = kin;
	}
	public String getSealName() {
		return sealName;
	}
	public void setSealName(String sealName) {
		this.sealName = sealName;
	}
	public String getToneName() {
		return toneName;
	}
	public void setToneName(String toneName) {
		this.toneName = toneName;
	}
	public String[] getCodeArrays() {
		return codeArrays;
	}
	public void setCodeArrays(String[] codeArrays) {
		this.codeArrays = codeArrays;
	}
	
	
}
